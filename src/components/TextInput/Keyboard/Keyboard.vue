<script>
/** vuex */
import { mapState, mapGetters } from 'vuex'

/** components */

/** mixins */

/** helpers */
import plMap from '@/keyboardMaps/pl'

export default {
  name: 'KeyBoard',
  props: {},
  data () {
    return {
      map: plMap
    }
  },
  computed: {
    ...mapState({
      carriagePosition: state => state.Tracker.carriagePosition
    }),
    ...mapGetters(['activeExpression']),
    highlightedKey () {
      const character = this.activeExpression.heads.charAt(this.carriagePosition)
      const keyCodes = this.map[character].split('+')
      return keyCodes
    }
  },
  watch: {
    highlightedKey: function (newKeys, oldKeys) {
      this.snuffKey(oldKeys)
      this.highlightKey(newKeys)
    }
  },
  methods: {
    animateHighlightKey (keyCode) {
      // in case the key was highlighted
      this.snuffKey(keyCode)
      // now play the animation
      this.$refs[keyCode].classList.add('animateHighlightKey')
      var highlightTimer = setTimeout(() => {
        this.$refs[keyCode].classList.remove('animateHighlightKey')
        clearTimeout(highlightTimer)
      }, 500)
    },
    highlightKey (keyCodes) {
      if (typeof keyCodes === 'object') {
        keyCodes.forEach(keyCode => {
          this.$refs[keyCode].classList.add('highlightKey')
        })
      } else {
        this.$refs[keyCodes].classList.add('highlightKey')
      }
    },
    snuffKey (keyCodes) {
      if (typeof keyCodes === 'object') {
        keyCodes.forEach(keyCode => {
          this.$refs[keyCode].classList.remove('highlightKey')
        })
      } else {
        this.$refs[keyCodes].classList.remove('highlightKey')
      }
    }
  },
  mounted () {
    this.highlightKey(this.highlightedKey)
    document.addEventListener('keypress', event =>
      this.animateHighlightKey(event.code)
    )
  }
}
</script>

